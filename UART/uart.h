/*
    UART header file
    @brief - UART library for ATmega328p microcontroller
*/

#ifndef UART_H
#define UART_H

/******************************************************************************
 * Includes                                                                   *
 ******************************************************************************/

#include <stdint.h>
#include <avr/io.h>
#include <avr/interrupt.h>

/******************************************************************************
 * Defines                                                                    *
 ******************************************************************************/

#ifndef FOSC
#define FOSC		16000000UL
#endif

#define BAUDRATE	9600
#define RX			0x01
#define TX			0x02
#define UBRR		(FOSC/16)/BAUDRATE - 1
#define DATA		0x06

/******************************************************************************
 * @brief - Initializes the UART                                              *
 ******************************************************************************/

void UART_Init();

/******************************************************************************
 * @brief - Sends one byte                                                    *
 ******************************************************************************/

void UART_SendByte(uint8_t);

/******************************************************************************
 * @brief - Sends string                                                    *
 ******************************************************************************/

void uartSend(const char* str);

/******************************************************************************
 * @brief - Receives one byte 												  *
 ******************************************************************************/

uint8_t UART_ReceiveByte();

#endif
